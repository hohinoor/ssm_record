package com.entity;

public class KeyParameters {
    private Integer id;

    private Float blocktime;

    private Float shootgluetime;

    private Float melttime;

    private Float meltterminus;

    private Float pressureswitch;

    private Float shootglueterminus;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Float getBlocktime() {
        return blocktime;
    }

    public void setBlocktime(Float blocktime) {
        this.blocktime = blocktime;
    }

    public Float getShootgluetime() {
        return shootgluetime;
    }

    public void setShootgluetime(Float shootgluetime) {
        this.shootgluetime = shootgluetime;
    }

    public Float getMelttime() {
        return melttime;
    }

    public void setMelttime(Float melttime) {
        this.melttime = melttime;
    }

    public Float getMeltterminus() {
        return meltterminus;
    }

    public void setMeltterminus(Float meltterminus) {
        this.meltterminus = meltterminus;
    }

    public Float getPressureswitch() {
        return pressureswitch;
    }

    public void setPressureswitch(Float pressureswitch) {
        this.pressureswitch = pressureswitch;
    }

    public Float getShootglueterminus() {
        return shootglueterminus;
    }

    public void setShootglueterminus(Float shootglueterminus) {
        this.shootglueterminus = shootglueterminus;
    }
}